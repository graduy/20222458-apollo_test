#!/bin/bash

# 创建构建目录（如果不存在）
if [! -d "build" ]; then
    mkdir build
fi

# 进入构建目录
cd build

# 运行cmake命令来配置项目，根据你的实际项目情况，你可能需要调整这里的参数，比如添加一些-D定义的变量等
cmake..

# 执行make命令进行编译，你可以根据需求添加 -j参数指定并行编译的任务数量，例如 -j4 表示用4个任务并行编译，加快速度
make

# 如果编译成功，可根据需要添加后续操作，比如运行生成的可执行文件等
# 例如假设生成的可执行文件叫myapp，可以在这里添加类似下面的语句来运行（先注释掉，按需启用）
#./myapp

# 退出构建目录
cd..